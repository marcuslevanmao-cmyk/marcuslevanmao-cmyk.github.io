// 6. RESTORE — replaces the live editor's tabs with a saved snapshot
// ============================================================

window.restoreFullDocumentState = function (savedTabsState) {
    if (typeof EditorEngine === 'undefined' || !savedTabsState || !savedTabsState.length) return;
    const liveTabs = EditorEngine.getTabs();
    liveTabs.length = 0;
    savedTabsState.forEach((tabState, idx) => {
        liveTabs.push({
            id: tabState.id || `tab_${Date.now()}_${idx}`,
            title: tabState.title || `Tab ${idx + 1}`,
            htmlContent: tabState.content || '<div></div>'
        });
    });
    // switchTab() is what actually updates EditorEngine's private activeTabId.
    // Calling renderTabsSidebar()/loadActiveTabContent() directly here (as before)
    // left activeTabId pointing at the stale default "tab_1", which no longer existed
    // in the freshly-restored tab list — loadActiveTabContent() would silently find
    // nothing and render a blank page until the user manually clicked a tab (which
    // calls switchTab() and only then fixes activeTabId).
    EditorEngine.switchTab(savedTabsState[0].id);
};

// ============================================================
// 7. AUTO-INIT on DOM ready
// ============================================================

function initHistory() {
    renderHistorySidebar();
    if (currentHistory.length > 0) {
        previewVersion(currentHistory[0].id);
        const latest = currentHistory[0];
        if (latest && latest.tabsState) {
            window.restoreFullDocumentState(latest.tabsState);
        }
    }
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initHistory);
} else {
    initHistory();
}
